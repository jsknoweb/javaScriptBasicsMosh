
let x = 10;
let y = 3;

console.log(x + y);
console.log(x - y);
console.log(x / y);
console.log(x * y);
console.log(x % y);
console.log(x ** y); // x to the power of y --> 10^3

console.log('---------------');
// Increment (++)
console.log(x);
console.log(x++);
console.log(x);
console.log(++x);
console.log(x);

console.log('---------------');
// Decrement 
console.log(x);
console.log(x--);
console.log(x);
console.log(--x);
console.log(x);